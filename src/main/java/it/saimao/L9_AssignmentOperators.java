package it.saimao;

/*
Compound assignment operators
+=
-=
*=
/=
%=
 */
public class L9_AssignmentOperators {
    public static void main(String[] args) {

        int x = 10;
        System.out.println(x);
        System.out.println(x += 5); // x = x + 5;
        System.out.println(x -= 10);
        System.out.println(x *= 2);
        System.out.println(x /= 2);
    }
}

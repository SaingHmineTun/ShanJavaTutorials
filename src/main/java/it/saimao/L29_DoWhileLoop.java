package it.saimao;

public class L29_DoWhileLoop {
    public static void main(String[] args) {

        // 1. Initialize value
        int i = 0;
        do {
            System.out.println("I is " + i);
            // 2. Update value
            i += 5;
        } while (i <= 100); // 3. Boolean expression

    }
}

package it.saimao;

public class L1_VariablesAndLiterals {
    public static void main(String[] args) {
        // Datatype variableName
        int number;

        System.out.println(123); // Integer literals
        System.out.println('း'); // Character literals
        System.out.println("မႂ်ႇသုင်ၶႃႈ"); // String literals
        System.out.println(false); // Boolean literals

        number = 1234; // Assign literal to variable
        System.out.println(number);

        String personIsSaiMao = "Sai Mao";
        personIsSaiMao = "1234"; // Java is static typed programming

    }
}